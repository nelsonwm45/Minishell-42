#include "libft.h"

int ft_numlen(int num)
{
    int len = 0;

    if (num == 0)
        return 1;

    if (num < 0)
    {
        len++;
        num = -num;
    }

    while (num > 0)
    {
        len++;
        num /= 10;
    }

    return len;
}